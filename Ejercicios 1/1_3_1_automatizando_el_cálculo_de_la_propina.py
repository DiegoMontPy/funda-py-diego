total_cuenta = float(input("Introduce el total de la cuenta: "))
porcentaje_propina = int(input("Introduce el porcentaje de propina (ej. 10, 15, 20): "))

propina = total_cuenta * (porcentaje_propina / 100)
total_a_pagar = total_cuenta + propina

print('Total de la cuenta: $', total_cuenta)
print('Propina: $', propina)
print('Total de la cuenta con propina: $', total_a_pagar)